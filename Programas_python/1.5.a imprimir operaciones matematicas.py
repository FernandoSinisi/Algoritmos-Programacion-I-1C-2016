def imprimir_operaciones(a,b):
	suma=a+b
	resta=a-b
	producto=a*b
	division=a/b
	print('La suma de los numeros {} y {} es: {}'.format(a,b,suma))
	print('La resta de los numeros {} y {} es: {}'.format(a,b,resta))
	print('El producto de los numeros {} y {} es: {}'.format(a,b,producto))
	print('La division de los numeros {} y {} es: {}'.format(a,b,division))

	
imprimir_operaciones(15,10)


